package chapter_5;

public class While {
    public static void main(String[] args) {
        // loop Control Instruction
        // type of loops
        // 1 while loop
        // 2 do-while loop
        // 3 for loop
        int i = 100;
        // this keep executing as long as the condition is true
        while (i <= 200) {
            System.out.println(i);
            i++;

        }
        System.out.println("end");
    }

}
