package Chapter_04;

public class Cwh_16_conditionals {
    public static void main(String[] args) {
        // if - else condition
        // switch condition
        int age = 18;
        boolean cond = (age >= 18);
        if (cond) {
            System.out.println("Adult");
        } else {
            System.out.println("Not Adult");
        }
        // Relational Operators
        // == --> equals
        // >= --> greater than equals to
        // > --> greater than
        // < --> less than
        // <= --> less than equals to
        // != --> not equals
        // = --> assignment

    }

}
